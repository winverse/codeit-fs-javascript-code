// 세미콜론(Semicolon) 실습

// 1. 세미콜론을 생략할 수 있는 경우
// 각 문장이 다른 줄에 있으면 자바스크립트가 자동으로 세미콜론을 삽입합니다.
console.log("첫 번째 문장");
console.log("두 번째 문장");

// 2. 세미콜론이 반드시 필요한 경우
// 아래 코드는 한 줄에 여러 문장을 작성하여 에러가 발생합니다.
// console.log('첫 번째 문장') console.log('두 번째 문장') // SyntaxError 발생

// 각 문장의 끝에 세미콜론을 추가하여 문장을 구분해주면 정상적으로 동작합니다.
console.log("첫 번째 문장");
console.log("두 번째 문장");

// 학습 초기에는 문장의 끝을 명확히 하고, 예상치 못한 오류를 줄이기 위해
// 세미콜론을 붙이는 습관을 들이는 것이 좋습니다.
console.log("세미콜론을 붙이는 것을 생활화 합시다!");
