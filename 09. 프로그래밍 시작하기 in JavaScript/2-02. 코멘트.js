// 03. 코멘트(Comment) 실습

// --- 1. 한 줄 코멘트 (Single-line Comment) ---
console.log("--- 1. 한 줄 코멘트 (Single-line Comment) ---");

// 이 줄은 전체가 코멘트입니다.

console.log("Hello, World!"); // 코드 오른쪽의 이 부분만 코멘트입니다.

// console.log('이 코드는 실행되지 않습니다.');

// --- 2. 여러 줄 코멘트 (Multi-line Comment) ---
console.log("\n--- 2. 여러 줄 코멘트 (Multi-line Comment) ---");

/*
  이것은
  여러 줄
  코멘트입니다.
*/
console.log("코멘트 밖의 코드는 정상적으로 실행됩니다.");

