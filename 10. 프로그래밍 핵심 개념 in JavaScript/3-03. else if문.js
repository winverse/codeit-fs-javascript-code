// if문 중첩하기 - 시간에 따른 인사말 예제
let hour = 14; // 현재 시간 (24시간 기준)

if (hour < 6) {
  console.log('안녕히 주무세요!');
} else {
  if (hour < 12) {
    console.log('좋은 아침입니다!');
  } else {
    if (hour < 18) {
      console.log('안녕하세요!');
    } else {
      console.log('좋은 저녁입니다!');
    }
  }
}

