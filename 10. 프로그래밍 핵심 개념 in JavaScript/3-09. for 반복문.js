// for 반복문 - 1부터 10까지 출력하기 예제
for (let i = 1; i <= 10; i++) {
  console.log(i);
}

// ---

// for 반복문 - 1부터 10까지의 합계 계산하기 예제
let sum = 0; // 합계를 저장할 변수

for (let i = 1; i <= 10; i++) {
  sum = sum + i; // 또는 sum += i;
}

console.log(sum); // 결과: 55
